#![allow(dead_code)]


// A Graph object represents a directed graph and is used in
// XRay to compute and store function call graphs and associated
// statistical information.
struct Graph {}

impl Graph {
  pub fn new() {}
  pub fn clear() {}
  pub fn vertices() {}
  pub fn edges() {}
  pub fn out_edges() {}
  pub fn is_edges() {}
  pub fn at() {}
  pub fn count() {}
  pub fn insert() {}
}